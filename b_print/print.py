# 출력 함수
# 오류를 구체화 할 때 사용한다.
print("주현진")

print("주현진", end=", ")
print("20살")
print("주현진", "20살", sep=", ")
# sep=", " 분류 하라 뭘로? 쌍 따옴표 안에 있는 ,로 앞에 두 개를

#제어 문자
# 반드시 따옴표 안에서 작성
# ctrl + d = 위에 줄 복사
print("한동석\n20살")
# \n => new line 뒤에 내용을 다음 줄에 출력
print("이름: \t한동석")
# \t => tab키랑 같은 효과

print("나이: \t20살")
print("\"한동석\"", "\'20살\'", sep=", ")
